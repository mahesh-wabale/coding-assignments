#include<stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	MyStrSet
//
//		Parameters		:	Address of string , character
//
//		returns			:	Address of string
//
//		Description		:	This function sets all characters in the string to a specific character.
//
////////////////////////////////////////////////////////////////////////////////////////////////
char* MyStrSet(char *ptr1,char ch)
{
	char *ptr2=ptr1;	
	
	while(*ptr1 != '\0')
	{
		*ptr1=ch;
		ptr1++;
	}

	return ptr2;
}

int main()
{
	char string[30]={'\0'};
	char ch;

	//accept string from user
	printf("\n Enter string => ");
	gets(string);

	printf("\n Enter character => ");
	scanf("%c",&ch);

	printf("%s",MyStrSet(string,ch));
	
	return 0;
}
