#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name :		PrintMaxOfTwoNum
//
//		Parameters	  :		integer , integer
//
//		Returns		  :		void
//
//		Description	  :		This function accepts two numbers and print the max number.
//
//////////////////////////////////////////////////////////////////////////////////////////
void PrintMaxOfTwoNum(int Num1 , int Num2)
{
	if( Num1 > Num2 )
	{
		printf("\n %d is maximum number.",Num1);
	}
	else
	{
		printf("\n %d is maximum number.",Num2);	
	}
}


int main()
{
	int iNo1,iNo2;

	printf("\n Enter two numbers => ");
	scanf("%d %d",&iNo1,&iNo2);

	PrintMaxOfTwoNum(iNo1,iNo2);

	return 0;
}


////////////////////////////////////////////////////////////////////////////////////////////
//
//		Input		:		Enter two numbers => 7 4
//
//		Output		:		7 is maximum number.
