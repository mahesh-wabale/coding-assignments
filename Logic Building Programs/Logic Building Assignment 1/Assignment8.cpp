#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name :		PrintEvenNumUptoN
//
//		Parameters	  :		integer
//
//		Returns		  :		void
//
//		Description	  :		This function accept number and print the all even numbers 
//							upto N.
//
//////////////////////////////////////////////////////////////////////////////////////////
void PrintEvenNumUptoN(int Num)
{
	register int i;

	for(i=1;i<=Num;i++)
	{
		if( i % 2 == 0)
		{
			printf("\t %d",i);
		}
	}
}


int main()
{
	int iNo;

	printf("\n Enter the number => ");
	scanf("%d",&iNo);

	PrintEvenNumUptoN(iNo);

	return 0;
}


