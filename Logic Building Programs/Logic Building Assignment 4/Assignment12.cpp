#include<stdio.h>

//Function Protptype
int PrintNumOfDigit(long int);

int main()
{
	//declaring integer
	long int iNum=0;

	printf("\nEnter Number => ");
	scanf("%d",&iNum);

	// Function call
	iNum=PrintNumOfDigit(iNum);
	printf("Number of digits are => %d",iNum);

	printf("\n");
	return 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	PrintNumOfDigit
//
//		Input			:	Integer
//
//		Returns			:	Integer(count)
//
//		Description		:	This function accept number from user and print number of digits of that number using recursion.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int PrintNumOfDigit(long int iNo)
{
	static int iCnt=0;
	if(iNo > 0)
	{
		iNo=iNo / 10;
		iCnt++;
		PrintNumOfDigit(iNo);		
	}
	return iCnt;
}
