#include<stdio.h>

//Function Protptype
void PrintNumLine(int);

int main()
{
	int iNum=0;

	printf("\nEnter Number of N => ");
	scanf("%d",&iNum);

	printf("\nNumber Line is=> \n");
	
	// Function call
	PrintNumLine(iNum);
	
	printf("\n");
	return 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:		PrintNumLine
//
//		Input			:		Integer
//
//		Return			:		void
//
//		Description		:		Accept  number and print the number line of that number using recursion.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void PrintNumLine(int iNo)
{
	static int icount=-iNo;
	
	if(icount<=iNo)
	{
		printf("%d\t",icount);
		icount++;
		PrintNumLine(iNo);
	}
}

