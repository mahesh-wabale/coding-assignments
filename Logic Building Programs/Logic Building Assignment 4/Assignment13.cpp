#include<stdio.h>

//Function Protptype
int PrintDigitsSum(long int);

int main()
{
	//declaring integer
	long int iNum=0;

	printf("\nEnter Number => ");
	scanf("%d",&iNum);

	// Function call
	iNum=PrintDigitsSum(iNum);
	printf("Sum of digits is => %d",iNum);

	printf("\n");
	return 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	PrintDigitsSum
//
//		Input			:	Integer
//
//		Returns			:	Integer(sum)
//
//		Description		:	This function accept number from user and print sum of that number using recursion.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int PrintDigitsSum(long int iNo)
{
	static int iSum=0;
	int iTemp=0;
	if(iNo > 0)
	{
		iTemp=iNo % 10;
		iNo=iNo / 10;
		iSum+=iTemp;
		PrintDigitsSum(iNo);		
	}
	return iSum;
}
