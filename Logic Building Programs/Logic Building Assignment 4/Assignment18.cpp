#include<stdio.h>

//Function Protptype
void PrintFactors(int);

int main()
{
	int iNum=0;

	printf("\nEnter Number => ");
	scanf("%d",&iNum);

	printf("\nFactors are => \n");
	
	// Function call
	PrintFactors(iNum);

	printf("\n");
	return 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	PrintFactors
//
//		Input			:	Integer
//
//		Returns			:	void
//
//		Description		:	This function accept number from user and print all factors of that number using recursion.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void PrintFactors(int iNo)
{
	static int iNum=1;
	if(iNum <= (iNo/2))
	{
		if((iNo % iNum) == 0)
		{
			printf("%d\t",iNum);
			iNum++;
		}
		else
		{
			iNum++;
		}
		PrintFactors(iNo);
	}
}
