void  PrintFibo(int iNo)
{
	static int iTemp1=1;
	static int iTemp2=0;
	static int iTemp3=0;
	if(iNo > 0)
	{
	
		printf("%d\t",iTemp3);
		iTemp2=iTemp1;
		iTemp1=iTemp3;
		
		iTemp3=iTemp1+iTemp2;
		
		iNo--;
		PrintFibo(iNo);
	}
}

