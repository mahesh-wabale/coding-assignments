#include<stdio.h>

//Function Protptype
int PrintStringLen(char *);

int main()
{
	//declaring integer
	char cString[50]={'\0'};
	long int iTemp=0;

	printf("\nEnter String => ");
	scanf("%[^\n]",cString);

	// Function call
	iTemp=PrintStringLen(cString);
	
	printf("String length is => %d\n",iTemp);
	
	return 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	PrintStringLen
//
//		Input			:	Address of string
//
//		Returns			:	Integer(length)
//
//		Description		:	This function accept string and count length of string using recursion.
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int PrintStringLen(char *cPtr)
{
	static int iCnt=0;
	if(*cPtr !=  '\0')
	{
		iCnt++;
		cPtr++;
		PrintStringLen(cPtr);
	}
		return iCnt;
}

