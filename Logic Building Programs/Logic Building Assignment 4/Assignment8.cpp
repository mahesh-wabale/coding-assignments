#include<stdio.h>

// Function Prototype

void PrintPattern(int);

int main()
{
	//declaring integer
	int iRow=0;

	printf("\nEnter Row => ");
	scanf("%d",&iRow);

	// Function call
	PrintPattern(iRow);

	printf("\n");
	return 0;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function Name	:	PrintPattern
//
//		Input			:	Integer
//
//		Output			:	void
//
//		Description		:	This function accept row from user and print pattern using recursion
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void PrintPattern(int iRow)
{
	static int iNum=1;
	static int iNo=1;
	if(iRow >0)
	{
		for(int iCol=1;iCol <= iNum;iCol++,iNo++)
		{
			printf("%d\t",iNo);
		}
		printf("\n");
		iNum++;
		iRow--;
		PrintPattern(iRow);
	}
}

