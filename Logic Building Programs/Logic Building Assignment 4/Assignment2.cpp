#include<stdio.h>

// Function Prototype
void PrintChar();

int main()
{
	// Function call
	PrintChar();
	printf("\n");
	return 0;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//		Function	:	PrintChar
//
//
//		return		:	void
//
//		Description	:	This function print 'a' To 'z' character by Using Recursion.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void PrintChar()
{
	static char cChar='a';
	if(cChar<= 'z')
	{
		printf("%c\t",cChar);
		cChar++;
		PrintChar();
	}
}

